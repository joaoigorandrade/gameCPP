Map::Map() {
	dirty = TextureManager::LoadTexture("res/gfx/dirty.png");
	grass = TextureManager::LoadTexture("res/gfx/grass.png");
	water = TextureManager::LoadTexture("res/gfx/water.png");

	LoadMap(level1);

	source.x = source.y = destination.x = destination.y = 0;
	source.w = source.h = destination.w = destination.h = 32;
}

void Map::LoadMap(int array[20][25]) {
	for (int row = 0; row < 20; row++) {
		for (int column = 0; column < 25; column++) {
			map[row][column] = array[row][column];
		}
	}
}

void Map::DrawMap() {
	int type = 0;
	for (int row = 0; row < 20; row++) {
		for (int column = 0; column < 25; column++) {
			type = map[row][column];
			destination.x = column * 32;
			destination.y = row * 32;
			switch (type) {
				case 0:
				TextureManager::Draw(water, source, destination);
				break;
				case 1:
				TextureManager::Draw(dirty, source, destination);
				break;
				case 2:
				TextureManager::Draw(grass, source, destination);
				break;
				default:
				break;
			}
		}
	}
}